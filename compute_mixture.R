ratval <- function(p,x) {
  # Evaluate rational function at x from coefficients p
  
  m=which(p==1)[1];
  
  a=p[1:m];
  
  b=p[(m+1):length(p)];
  
  y=polyval(a,x)/polyval(b,x);
  
  return(y)
}